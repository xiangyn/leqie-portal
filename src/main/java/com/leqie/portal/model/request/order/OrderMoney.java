package com.leqie.portal.model.request.order;

public class OrderMoney {

	private String chang;
	
	private String kuang;
	
	private String hou;
	
	private String type;
	
	private String amount;
	
	private String zhonglei;
	
	private String erjimulu;

	public String getChang() {
		return chang;
	}

	public void setChang(String chang) {
		this.chang = chang;
	}

	public String getKuang() {
		return kuang;
	}

	public void setKuang(String kuang) {
		this.kuang = kuang;
	}

	public String getHou() {
		return hou;
	}

	public void setHou(String hou) {
		this.hou = hou;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getZhonglei() {
		return zhonglei;
	}

	public void setZhonglei(String zhonglei) {
		this.zhonglei = zhonglei;
	}

	public String getErjimulu() {
		return erjimulu;
	}

	public void setErjimulu(String erjimulu) {
		this.erjimulu = erjimulu;
	}
	
}
